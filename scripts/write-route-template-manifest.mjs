import { mkdirSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '..');
const pagesDir = resolve(repoRoot, 'pages');
const apiDir = resolve(repoRoot, 'server/api');
const outputPath = resolve(repoRoot, 'server/utils/meta/routeTemplates.ts');
const HTTP_METHOD_SUFFIX_PATTERN =
    /\.(connect|delete|get|head|options|patch|post|put|trace)$/i;
const SUPPORTED_DYNAMIC_SEGMENT_PATTERN = /^\[[A-Za-z0-9_]+\]$/;

function toPosixPath(path) {
    return path.split(sep).join('/');
}

function walkFiles(rootDir) {
    const filePaths = [];
    const queue = [rootDir];

    while (queue.length > 0) {
        const currentDir = queue.pop();
        const entries = readdirSync(currentDir, { withFileTypes: true });

        for (const entry of entries) {
            const entryPath = resolve(currentDir, entry.name);
            if (entry.isDirectory()) {
                queue.push(entryPath);
                continue;
            }

            filePaths.push(entryPath);
        }
    }

    return filePaths;
}

function parseRouteSegment(segment, filePath) {
    if (!segment.includes('[')) {
        return segment;
    }

    if (!SUPPORTED_DYNAMIC_SEGMENT_PATTERN.test(segment)) {
        throw new Error(
            `Unsupported dynamic route segment "${segment}" in ${filePath}`
        );
    }

    return `:${segment.slice(1, -1)}`;
}

function buildRoutePathFromSegments(segments) {
    if (segments.length <= 0) {
        return '/';
    }

    return `/${segments.join('/')}`;
}

function toPageTemplate(filePath) {
    if (!filePath.endsWith('.vue')) {
        return null;
    }

    const relativePath = toPosixPath(relative(pagesDir, filePath));
    const rawSegments = relativePath.replace(/\.vue$/i, '').split('/');
    if (rawSegments[rawSegments.length - 1] === 'index') {
        rawSegments.pop();
    }

    const routeSegments = rawSegments
        .filter(Boolean)
        .map((segment) => parseRouteSegment(segment, relativePath));
    const template = buildRoutePathFromSegments(routeSegments);

    return template.includes('/:') ? template : null;
}

function toApiTemplate(filePath) {
    if (!/\.(?:[cm]?[jt]s)$/i.test(filePath)) {
        return null;
    }

    const relativePath = toPosixPath(relative(apiDir, filePath));
    const rawSegments = relativePath.replace(/\.(?:[cm]?[jt]s)$/i, '').split('/');
    const lastSegment = rawSegments.pop();

    if (!lastSegment) {
        return null;
    }

    const routeFileSegment = lastSegment.replace(HTTP_METHOD_SUFFIX_PATTERN, '');
    if (routeFileSegment.length > 0) {
        rawSegments.push(routeFileSegment);
    }

    if (rawSegments[rawSegments.length - 1] === 'index') {
        rawSegments.pop();
    }

    const routeSegments = rawSegments
        .filter(Boolean)
        .map((segment) => parseRouteSegment(segment, relativePath));
    const template = buildRoutePathFromSegments(['api', ...routeSegments]);

    return template.includes('/:') ? template : null;
}

function compareTemplates(left, right) {
    const leftSegments = left === '/' ? 0 : left.split('/').length;
    const rightSegments = right === '/' ? 0 : right.split('/').length;
    if (rightSegments !== leftSegments) {
        return rightSegments - leftSegments;
    }

    const leftDynamicCount = (left.match(/\/:/g) ?? []).length;
    const rightDynamicCount = (right.match(/\/:/g) ?? []).length;
    if (leftDynamicCount !== rightDynamicCount) {
        return leftDynamicCount - rightDynamicCount;
    }

    return left.localeCompare(right);
}

function buildTemplateEntries(templates) {
    return Array.from(new Set(templates.filter(Boolean)))
        .sort(compareTemplates)
        .map((template) => {
            const segments =
                template === '/' ? [] : template.split('/').filter(Boolean);

            return {
                template,
                segments
            };
        });
}

function renderTemplateEntries(entries) {
    if (entries.length <= 0) {
        return '[]';
    }

    const lines = ['['];
    for (const entry of entries) {
        const segmentList =
            entry.segments.length > 0
                ? `[${entry.segments.map((segment) => `'${segment}'`).join(', ')}]`
                : '[]';
        lines.push(
            `        { template: '${entry.template}', segments: ${segmentList} },`
        );
    }
    lines.push('    ]');
    return lines.join('\n');
}

function renderOutput(ssrTemplates, apiTemplates) {
    return `// Generated by scripts/write-route-template-manifest.mjs
export interface AdminServerMetricsRouteTemplate {
    template: string;
    segments: string[];
}

export const adminServerMetricsRouteTemplates = {
    ssr: ${renderTemplateEntries(ssrTemplates)},
    api: ${renderTemplateEntries(apiTemplates)}
} satisfies Record<'ssr' | 'api', AdminServerMetricsRouteTemplate[]>;
`;
}

function writeOutputFile(content) {
    mkdirSync(dirname(outputPath), { recursive: true });
    writeFileSync(outputPath, content, 'utf8');
}

function main() {
    const ssrTemplates = buildTemplateEntries(
        walkFiles(pagesDir).map(toPageTemplate)
    );
    const apiTemplates = buildTemplateEntries(
        walkFiles(apiDir).map(toApiTemplate)
    );

    writeOutputFile(renderOutput(ssrTemplates, apiTemplates));
}

main();
