import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '..');

const outputPaths = [
    resolve(repoRoot, 'utils/meta/commit.ts'),
    resolve(repoRoot, 'server/utils/meta/commit.ts')
];

function readCommitId() {
    const output = execFileSync('git', ['rev-parse', 'HEAD'], {
        cwd: repoRoot,
        encoding: 'utf8'
    }).trim();

    if (!/^[0-9a-f]{40}$/i.test(output)) {
        throw new Error(`Unexpected commit id: ${output}`);
    }

    return output;
}

function renderCommitFile(commitId) {
    return `// Generated by scripts/write-commit-id.mjs
const commitId = '${commitId}';

export default commitId;
`;
}

function writeCommitFile(filePath, content) {
    mkdirSync(dirname(filePath), { recursive: true });
    writeFileSync(filePath, content, 'utf8');
}

function main() {
    const commitId = readCommitId();
    const content = renderCommitFile(commitId);

    for (const outputPath of outputPaths) {
        writeCommitFile(outputPath, content);
    }
}

main();
